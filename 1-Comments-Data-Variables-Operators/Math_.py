import math #importing the builtin math module.

# Constants
math.pi   # Mathematical constant Ï€ (pi)
math.e    # Mathematical constant e (base of natural logarithms)
math.inf  # A floating-point positive infinity
math.nan  # A floating-point "not a number" (NaN)


# Trigonometric Functions
math.sin(0)       # Returns the sine of x (in radians).
math.cos(0)       # Returns the cosine of x (in radians).
math.tan(0)       # Returns the tangent of x (in radians).
math.asin(0)      # Returns the arc sine of x, in radians.
math.acos(0)      # Returns the arc cosine of x, in radians.
math.atan(0)      # Returns the arc tangent of x, in radians.
math.atan2(0, 1)  # Returns the arc tangent of y/x, in radians, while respecting the quadrant signs of both inputs.

# Hyperbolic Functions
math.sinh(0)   # Returns the hyperbolic sine of x.
math.cosh(0)   # Returns the hyperbolic cosine of x.
math.tanh(0)   # Returns the hyperbolic tangent of x.
math.asinh(0)  # Returns the inverse hyperbolic sine of x.
math.acosh(1)  # Returns the inverse hyperbolic cosine of x.
math.atanh(0)  # Returns the inverse hyperbolic tangent of x.

# Exponential and Logarithmic Functions
math.exp(1)      # Returns e raised to the power of x.
math.log(2)      # Returns the natural logarithm of x (to base e).
math.log10(100)  # Returns the base-10 logarithm of x.
math.log2(8)     # Returns the base-2 logarithm of x.
math.pow(2, 3)   # Returns x raised to the power of y.

# Other Functions
math.sqrt(4)             # Returns the square root of x.
math.ceil(3.14)          # Returns the ceiling of x, the smallest integer greater than or equal to x.
math.floor(3.14)         # Returns the floor of x, the largest integer less than or equal to x.
math.factorial(5)        # Returns the factorial of x.
math.isqrt(16)           # Returns the integer square root of n.
math.gcd(12, 18)         # Greatest common divisor of a and b
math.degrees(math.pi/2)  # Converts angle x from radians to degrees
math.radians(90)         # Converts angle x from degrees to radians
math.erf(0.5)            # Error function at x
math.erfc(0.5)           # Complementary error function at x
math.gamma(5)            # Gamma function at x
math.lgamma(5)           # Natural logarithm of the absolute value of the gamma function at x











